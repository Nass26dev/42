#include "../include/fdf.h"

void	my_mlx_pixel_put(t_param *p, int x, int y, int color)
{
	if (x >= 0 && x < 1000 && y >= 0 && y < 1000)
	{
		char	*dst;
		
		dst = p->data + (y * p->sl+ x * (p->bpp / 8));
		*(unsigned int *)dst = color;
	}
}
