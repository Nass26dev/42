#include "ft.h"

void	ft_write_board(int **board)
{
	int		i;
	int		j;
	char	c;

	i = 1;
	while (i < 5)
	{
		j = 1;
		while (j < 5)
		{
			c = board[i][j] + '0';
			write(1, &c, 1);
			write (1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
