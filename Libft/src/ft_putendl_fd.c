#include "../include/libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	c;

	c = '\n';
	i = -1;
	while (s[++i])
		write(fd, &s[i], 1);
	write(fd, &c, 1);
}
